** Cocher les propositions vraies
- Si $% a, b %$ sont des réels et $%a \le b %$, alors $% \frac{1}{a} \ge \frac{1}{b} %$
+ Si $% a, b %$ sont des réels strictement négatifs  et $%a \le b %$, alors $% \frac{1}{a} \ge \frac{1}{b} %$
- Si $%a,b %$ sont des réels et $%a \le b %$, alors $% a^2 \ge b^2 %$
+ Si $% a, b %$ sont des réels strictement négatifs  et $%a \le b %$, alors $% a^2 \ge b^2 %$
- Si $%f%$ est une fonction strictement croissante sur la partie $%I%$ et strictement croissante sur la partie $%J%$, alors $% f%$ est strictement croissante sur $%I \cup J%$.
+ Si $%f%$ est une fonction strictement croissante sur la partie $%I%$ et strictement croissante sur la partie $%J%$ et $% I \cap J \neq \emptyset %$, alors $% f%$ est strictement croissante sur $%I \cup J%$.



** Cocher les propositions vraies :
-  $% \forall a,b,c  \in \mathbb{R} \quad a \le b  \implies ac \le bc %$ 
+ $% \forall a,b  \in \mathbb{R}\; \forall c \in \mathbb{R}^+ \quad a \le b  \implies ac \le bc %$ 
+ $% \forall a,b,c  \in \mathbb{R} \quad a \le b  \implies a + c \le b+ c %$ 
- $% \forall a,b  \in \mathbb{R}\; \forall c \in \mathbb{R}^+ \quad a \le b  \implies \frac{a}{c} \le \frac{b}{c} %$ 





** Cocher les propositions vraies :
- Si $% f %$ est une fonction numérique croissante et $% I %$ est un intervalle borné, alors $%f(I)%$ est borné et $% \sup(f(I)) = f(\sup(I)) %$
- Si $% f %$ est une fonction numérique croissante et $% I %$ est un intervalle borné, alors $%f(I)%$ est borné et $% \sup(f(I)) > f(\sup(I)) %$
+ Si $% f %$ est une fonction numérique croissante et $% I %$ est une partie majorée, alors $%f(I)%$ est majorée et $% \sup(f(I)) \le  f(\sup(I)) %$
+  Si $% f %$ est une fonction numérique croissante et $% I %$ est une partie majorée qui admet un plus grand élément, alors $%f(I)%$ est majorée et $% \sup(f(I)) = f(\sup(I)) %$
  
 

