ecrire une fonction "mesure" qui retourne un tuple avec le nombre de ligne et d'augmentations.

exemple:
200 (première valeur )  
210 (augmentation)
215 (augmentation)
207 (diminution)
214 (augmentation)
216 (augmentation)
213 (diminution)

7 lignes
4 augmentations

