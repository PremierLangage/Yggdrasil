title = Liste composée

text==

Ecrire une fonction `create` à deux paramètres qui retourne la liste composée des deux paramètres.

Par exemple, l'appel `create(3,4)` doit renvoyer la liste `[3, 4]`.
==

tag=listCreate

pltest0==
>>> create(2,"4")
[2, '4']
>>> create(3.14,"titi")
[3.14, 'titi']
