title=Echange de deux variables
text==
On suppose qu'il existe deux variables `a` et `b` de valeurs quelconques précédemment fixées.
Ecrire un programme dont l'exécution échange les valeurs de `a` et `b`.

Attention, votre programe doit fonctionner quelles que soient les valeurs initiales de `a` et `b`.
