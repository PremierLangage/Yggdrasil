title=Premiers exercices sur la notion de programme

introduction==
Les exercices qui suivent illustrent la notion de programme en C. Ce sont des 
exercices simples, parfois des questions de cours, et de découverte du langage C.

Attention, le compilateur utilisé sera ici `gcc` mais ce dernier sera utilisé avec
les flags `-Wall` et `-std=c17`. Ainsi, même si les sandbox n'utilisent pas 
encore le compilateur `clang`, les exercices seront bien sur le standard C 2017.
