title==
Polynôme annulateur et projection
==

text==
<p>Soit $$E$$ un espace vectoriel de dimension finie et $$f$$ un endomorphisme de $$E$$ qui vérifie $$f\circ f\=f$$. On suppose que  $$f
eq Id_\{E\}$$ et $$f
eq0$$.</p>
<p>Cocher les assertions justes</p>
==

choices==
%33.33333%<p>$$f$$ est diagonalisable</p>
%33.33333%<p>$$P(X)\=X^2-X$$ est un polynôme annulateur de $$f$$.</p>
%-50.0%<p>$$P(X)\=X-1$$ est un polynôme annulateur de $$f$$.</p>
%33.33333%<p>Les valeurs propres de $$f$$ sont 0 et 1.</p>
%-50.0%<p>$$P(X)\=X$$ est un polynôme annulateur de $$f$$.</p>
