title==
definition de l'adjoint
==

text==
<p>Soit $$E$$ un espace euclidien de dimension finie. Cocher les assertions justes</p>
==

choices==
%50.0%<p>Tout endomorphisme défini sur $$E$$ admet un endomorphisme adjoint.</p>
%50.0%<p>Si $$f$$ et $$g$$ sont des endomorphismes de $$E$$ tels que pour tous vecteurs $$x,y\in E$$, $$\langle f(x),y\rangle\= \langle g(y),x\rangle$$</p>\n<p>alors $$g$$ est l'adjoint de $$f$$ et $$f$$ est l'adjoint de $$g$$.</p>\n<p> </p>\n<p> </p>
%-50.0%<p>Soit $$f$$ et $$g$$ des endomorphisme et $$f^*$$ et $$g^*$$ leurs adjoints respectifs. Alors $$f^*\circ g^*$$ est l'adjoint de $$f\circ g$$.</p>
%-50.0%<p>Soit $$f$$ un endomorphisme et $$f^*$$ son adjoint. Etant donnée une base quelconque $$\mathcal B$$ de $$E$$, la matrice de $$f^*$$ dans $$ \mathcal B$$ est la transposée de la matrice de $$f$$ dans $$ \mathcal B$$.</p>
