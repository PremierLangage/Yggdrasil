title==
polynôme annulateur et polynôme caractéristique
==

text==
<p>Soit $$A$$ une matrice à coefficients réels et $$P$$ un polynôme annulateur de $$A$$ (à coefficients réels).</p>
<p>Cocher les bonnes réponses.</p>
==

choices==
%-33.33333%<p>Si $$\lambda$$ est une racine de $$P$$ alors $$\lambda$$ est une racine de $$\chi_A $$.</p>
%33.33333%<p>Si $$\lambda$$ est valeur propre de $$A$$ alors $$\lambda$$ est une racine de $$P$$.</p>
%33.33333%<p>Si $$P$$ est scindé et à racines simples  alors $$\chi_A$$ est scindé.</p>
%-33.33333%<p>Si $$\chi_A$$ est scindé alors $$P$$ est scindé.</p>
%-33.33333%<p>Le théorème de Cayley-Hamilton implique que tout polynôme annulateur de $$A$$ est divisible par $$\chi_A$$.</p>
%33.33333%<p>Si $$A$$ est diagonalisable et admet $$k$$ valeurs propres distinctes alors il existe un polynôme annulateur de $$A$$ de degré exactement $$k$$.</p>\n<p> </p>
==