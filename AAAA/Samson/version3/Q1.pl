title==
Inégalités (produit scalaire)
==

text==
<p>Soient $$E$$ un espace euclidien. On note $$\langle\cdot,\cdot\rangle$$ son produit scalaire et $$\|\cdot\|$$ sa norme associée. Donner le nom correct à chacune des identités ou inégalités suivantes</p>
==

choices==
=<p>$$(\langle x,y\rangle)^2\leqslant \|x\|^2\|y\|^2$$</p> -> Inégalité de Cauchy-Schwarz
=<p>$$\|x+y\|\leqslant \|x\|+\|y\|$$</p> -> Inégalité de Minkowski
=<p>$$\|x+y\|^2+\|x-y\|^2\=2(\|x\|^2+\|y\|^2)$$</p> -> Identité du parallélogramme
=<p>$$\langle x,y\rangle\=\frac14(\|x+y\|^2-\|x-y\|^2)$$</p> -> Identité de polarisation

==