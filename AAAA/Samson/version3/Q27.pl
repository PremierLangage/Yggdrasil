text==
<p>Soit $$E$$ un espace euclidien et $$F$$ un sous-espace vectoriel de $$E$$. On note $$p$$ la projection orthogonale sur $$F$$.</p>
<p>Cocher les assertions justes.</p>
==

choices==
%33.33333%<p>Si $$x\in F$$, alors $$p(x)\=x$$.</p>
%-33.33333%<p>Si $$x\in F$$, alors $$p(x)\=0$$.</p>
%33.33333%<p>La distance de $$x$$ à $$F$$ est donnée par $$d(x,F)\=\|x-p(x)\|$$</p>
%-33.33333%<p>La distance de $$x$$ à $$F$$ est donnée par $$d(x,F)\=\|p(x)\|$$</p>
%33.33333%<p>$$x$$ et $$x-p(x)$$ sont orthogonaux.</p>
==