text==
<p>Soient $$\mathcal\{E\}\=(e_1,\ldots, e_n)$$ et $$\mathcal\{F\}\=(f_1,\ldots, f_n)$$ deux bases d'un espace vectoriel $$E$$ de dimension $$n$$. Soit $$g$$ un endomorphisme de $$E$$.</p>
<p>On note $$A\=\mathrm\{Mat\}_\{\mathcal E\}(g)$$ et $$B\=\mathrm\{Mat\}_\{\mathcal F\}(g)$$.</p>
<p>Cocher les propositions justes</p>
==

choices==
%100.0%<p>$$det(g) \=\det(A)$$</p>
%-50.0%<p>$$det(A)\neq det(B)$$</p>
