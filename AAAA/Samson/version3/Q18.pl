choices==
%33.33333%<p>$$A$$ est nilpotente s'il existe un entier $$p$$ tel que $$A^p\=0$$.</p>
%33.33333%<p>$$A$$ est nilpotente si et seulement si $$A^n\=0$$.</p>
%33.33333%<p>Si $$A$$ est nilpotente alors $$A$$ est trigonalisable.</p>
==