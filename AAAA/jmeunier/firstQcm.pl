# un seul choix
* quelle est la définition de la moyenne ?
- la somme des observations divisée par le nombre de sujets
+ la somme des observations divisée par le nombre d'observations
- la somme des sujets divisée par le nombre de variables
- le valeur maximale divisée par le nombre de valeurs

# plusieurs choix
** Quelle est la définition de la médiane ?
- la valeur qui coupe la somme des observations en deux parties égales
+ la valeur pour laquelle 50% des observations sont inférieures
+ la valeur pour laquelle 50% des observations sont supérieures
- la valeur corrrespondant à la moitié de la distribution
